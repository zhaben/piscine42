#include <stdio.h>

char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (!(to_find[j]))
		return (str);
	while (str[i] != '\0')
	{
		j = 0;
		while (to_find[j] == str[i + j])
		{
			if (to_find[j + 1] == '\0')
			{
				return (str + i);
			}
			j++;
		}
		i++;
	}
	return (0);
}

int		main(int argc, char **argv)
{
	if (argc > 0)
	{
		printf("%s", ft_strstr(argv[1], argv[2]));
		printf("%s", ft_strstr(argv[3], argv[4]));
	}
	return (0);
}
